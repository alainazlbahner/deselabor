# Initial values
i = 5

# Assign the value of i to idx
idx = i

# Print values to verify
print("Value of i:", i)    # Output: Value of i: 5
print("Value of idx:", idx) # Output: Value of idx: 5

# Modify i and see the effect on idx
i = 10
print("Value of i after modification:", i)    # Output: Value of i after modification: 10
print("Value of idx remains the same:", idx)  # Output: Value of idx remains the same: 5
